/**A classe de Divisao é a responsável por executar a lógica matemática de divisão. Na sequência do código temos a declaração dessa classe e do método dividir.**/
//Classe da operação de divisão. Mudanças são sugeridas para tratar possíveis entradas e saídas.
public class Divisao 
{
	/**Esse é o método que executa a lógica matemática ao receber dois números como argumento. Os parâmetros desse método são dois números inteiros.**/
	public int dividir(int a, int b) 
	{
		/**Esse é o retorno da operação.**/
		return a / b;
	}
}
