/**A classe de Subtracao é a responsável por executar a lógica matemática de diferença. Na sequência do código temos a declaração dessa classe e do método subtrair.**/
//Classe da operação de subtração.
public class Subtracao 
{
	/**Esse é o método que executa a lógica matemática ao receber dois números como argumento. Os parâmetros desse método são dois números inteiros.**/
	public int subtrair(int a, int b) 
	{
		/**Esse é o retorno da operação.**/
		return a - b;
	}
}
