/**A classe de Soma é a responsável por executar a lógica matemática de adição. Na sequência do código temos a declaração dessa classe e do método somar.**/
//Classe da operação de adição.
public class Soma 
{
	/**Esse é o método que executa a lógica matemática ao receber dois números como argumento. Os parâmetros desse método são dois números inteiros.**/
	public int somar(int a, int b) 
	{
		/**Esse é o retorno da operação.**/
		return a + b;
	}
}
